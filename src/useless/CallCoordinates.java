package useless;


public class CallCoordinates {

    int x;

    int y;

    public CallCoordinates(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getY() {
        return y;
    }

    public int getX(){
        return x;
    };

}
